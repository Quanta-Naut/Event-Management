import { useCallback } from "react";
import { useLocation } from "wouter";

interface UseCommandBarOptions {
  onClose: () => void;
}

interface Command {
  id: string;
  type: "page" | "action";
  title: string;
  description: string;
  icon: React.ReactNode;
  action: () => void;
}

